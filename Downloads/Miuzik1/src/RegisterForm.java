    //create function to determin if the user already exists in the data base
    
    public boolean checkUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean usernameExists = false;
        
        String query = "SELECT * FROM `users` WHERE `username` = ?";
        
        try {
            st = My_CNX.getConnection().prepareStatement(query);
            st.setString(1, username);
            
            rs = st.executeQuery();
            
            if(rs.next()){
                usernameExists = true;
                JOptionPane.showMessageDialog(null, "User Already Exists, Choose Another Username", "Username Taken", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return usernameExists;
    }    
    
    //function to verify empty fields in Register Form 
    //check if password and confirm passwords are equal!!
    public boolean verifyEmptyFields(){
        
        String firstname = jTextField_FirstName.getText();
        String lastname = jTextField_LastName.getText();
        String username = jTextField_Username.getText();
        String password1 = String.valueOf(jPasswordField_Password.getPassword());
        String confirmpass = String.valueOf(jPasswordField_ConfirmPassword.getPassword());
        
        if(firstname.trim().equals("") || lastname.trim().equals("") || username.trim().equals("") ||
                password1.trim().equals("") || confirmpass.trim().equals("") || firstname.trim().equals("") ){
            
            JOptionPane.showMessageDialog(null, "One or more Fields Are empty", "Empty Fields Error", 2);
            return false;
           
        }
        else if(!(password1.equals(confirmpass))){
            JOptionPane.showMessageDialog(null, "Passwords don't match", "Confirm Password", 2);
            return false;
        }
        
        else{
            return true;
        }       
    }   
