    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        // TODO add your handling code here:
        String firstname = jTextField_FirstName.getText();
        String lastname = jTextField_LastName.getText();
        String username = jTextField_Username.getText();
        String password1 = String.valueOf(jPasswordField_Password.getPassword());
        String confirmpass = String.valueOf(jPasswordField_ConfirmPassword.getPassword());
        
        if(verifyEmptyFields()){
            if(!checkUsername(username)){
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery = "INSERT INTO `users`(`first_name`, `last_name`, `username`, `password`) VALUES (?,?,?,?)";
                
                try {
                    ps = My_CNX.getConnection().prepareStatement(registerUserQuery);
                    
                    ps.setString(1, firstname);
                    ps.setString(2, lastname);
                    ps.setString(3, username);
                    ps.setString(4, password1);
                    
                    if(ps.executeUpdate() != 0){
                        JOptionPane.showMessageDialog(null, "Your account has been created");
                        LoginForm log = new LoginForm();
                        log.setVisible(true);
                        log.pack();
                        log.setLocationRelativeTo(null);
                        log.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                        this.dispose(); //login form leaves the "frame"
                    } else{
                        JOptionPane.showMessageDialog(null, "Error Creating account");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                
               
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButtonRegisterActionPerformed

